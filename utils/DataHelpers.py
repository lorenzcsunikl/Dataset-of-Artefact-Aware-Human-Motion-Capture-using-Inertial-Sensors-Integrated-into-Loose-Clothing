import numpy as np
def getActivationArray(persons=list(range(1,14))):
    allP = []
    for iPerson in persons:
        beginning = 0
        activities  = {}
        for act in categLongTerm:
            activities[act] = np.empty((0,2), dtype=int)
        allP.append(activities)

        for (end,act) in dictPerson2Activ[iPerson]:
            allP[-1][act] = np.block( [ [allP[-1][act]],[np.array([beginning,end])] ] )
            beginning = end
    return allP


